package io.github.dovecoteescapee.byedpi.core

/**
 * Наборы флагов ByeDPI (ciadpi) для перебора. Флаги описаны в README v0.13:
 * https://github.com/hufrea/byedpi/blob/v0.13/README.md
 *
 * Только desync-флаги — слушатель (-i/-p) подставляет ByeDpiTester сам.
 *
 * ПОЗИЦИОННО-НЕЗАВИСИМЫЙ ПОДХОД (главный принцип этого файла)
 * ----------------------------------------------------------
 * DPI режет по блок-СЛОВУ в SNI ("youtube", "googlevideo", "instagram"…).
 * Одиночный `-dN+s` бьёт по ФИКСИРОВАННОМУ смещению N от начала имени, поэтому
 * ломает слово, только если оно случайно оказалось у смещения N. Для большого
 * списка блокировок слово стоит в РАЗНЫХ местах имени, и один N их не покрывает
 * (пример: "youtube" — смещение 4, "googlevideo" — смещение 11).
 *
 * Решение — КАСКАД разбиений по всей длине имени (`-d1+s -d5+s -d9+s …`): хотя бы
 * одно (а обычно несколько) разбиений попадает ВНУТРЬ любого блок-слова, где бы
 * оно ни стояло. README (стр. 78) разрешает несколько `-s/-d`, README (стр. 149)
 * требует, чтобы позиции шли ПО ВОЗРАСТАНИЮ — все каскады тут отсортированы.
 * tlsrec (`-r`) и fake (`-f`) тоже не зависят от позиции слова и идут в набор.
 */
object TestCommandGenerator {

    /** Каскад одного примитива (-d/-s/-r) по возрастающим смещениям +s. */
    private fun cascade(opt: String, first: Int, step: Int, max: Int): String =
        (first..max step step).joinToString(" ") { "-$opt$it+s" }

    /**
     * КОМБО «TCP-десинк + UDP-десинк» — единственный класс, дающий РЕАЛЬНО рабочий
     * вариант. Причина проста и подтверждена на устройстве:
     *   • страница YouTube идёт по TCP/TLS  → её пробивает -r/-s/-d;
     *   • само ВИДЕО идёт по QUIC (UDP-443) → его пробивает только -a (UDP-fake).
     * Поодиночке ни один класс не проходит ОБЕ решающие цели: tlsrec чинит страницу,
     * но не видео; -a чинит видео, но не страницу. Нужен их союз в ОДНОМ флаге.
     *
     * tcp-базы идут первыми tlsrec-каскадами: на stateful-DPI они позиционно-
     * независимо пробивают и youtube (слово@4), и googlevideo (слово@11). udp-хвосты
     * — те, что реально пробили QUIC на тесте (-a1/-a2 с TTL 4..8).
     */
    fun combos(): List<String> {
        val tcp = listOf(
            "-r1+s -r4+s -r7+s -r10+s -r13+s",
            "-r1+s -r5+s -r9+s -r13+s",
            "-r1+s -r5+s -r9+s -r13+s -r17+s -r21+s",
            "-r1+s -r4+s -r7+s -r10+s -r13+s -r16+s",
            // на случай «лёгкого» DPI — мягкий сплит-каскад как tcp-база
            "-s1+s -d3+s -d7+s -d11+s -d15+s -d19+s",
        )
        val udp = listOf("-a1 -t6", "-a1 -t8", "-a2 -t4", "-a1 -t4", "-a3 -t6")
        val out = LinkedHashSet<String>()
        for (t in tcp) for (u in udp) out.add("$t $u")
        return out.toList()
    }

    /**
     * Курируемый список: компактный, но уже ПОЗИЦИОННО-НЕЗАВИСИМЫЙ.
     * Сверху — самые универсальные каскады (ломают слово где угодно),
     * снизу — дешёвые одиночные (вдруг сеть «лёгкая») и UDP для видео.
     */
    fun curated(): List<String> = listOf(
        // --- КОМБО TCP+UDP (страница по TLS + видео по QUIC) — самый рабочий класс ---
        "-r1+s -r4+s -r7+s -r10+s -r13+s -a1 -t6",
        "-r1+s -r5+s -r9+s -r13+s -a1 -t8",
        "-r1+s -r4+s -r7+s -r10+s -r13+s -a2 -t4",
        // --- КАСКАДЫ: рвут SNI по всей длине, слово ломается независимо от места ---
        // Плотный дизордер до смещения 21 — покрывает и "youtube"(4), и "googlevideo"(11..21).
        "-d1+s -d5+s -d9+s -d13+s -d17+s -d21+s",
        // Сплит-граница SNI + дизордер-каскад (рекомендация README для Linux/Windows).
        "-s1+s -d3+s -d7+s -d11+s -d15+s -d19+s",
        // Каскад сплитов (мягче дизордера, меньше ретрансмиссий).
        "-s1+s -s4+s -s8+s -s12+s -s16+s -s20+s",
        // --- tlsrec-КАСКАД: режет TLS-запись в НЕСКОЛЬКИХ точках, разбор SNI
        //     ломается независимо от позиции слова. На практике — самый пробивной
        //     класс против SNI-блэкхола (подтверждено: пробивает и youtube, и
        //     googlevideo одним флагом). Разные плотности/глубины на случай сети. ---
        "-r1+s -r5+s -r9+s -r13+s",
        "-r1+s -r4+s -r7+s -r10+s -r13+s -r16+s",
        "-r1+s -r6+s -r11+s -r16+s -r21+s",
        // auto-gated: tlsrec применяется ТОЛЬКО когда DPI сбросил/затаймаутил —
        // безопаснее для «обычных» сайтов и мидлбоксов (README, стр. 229-232).
        "-A torst -T 3 -r1+s -r5+s -r9+s -r13+s",
        "-r1+s",
        // --- fake-ClientHello (декой), тоже позиционно-независим ---
        "-f1+s -t3",
        "-d1+s -f-1 -t3",                       // disorder+fake — README-стиль (без md5sig на Android)
        "-f1+s -t4 -d6+s -d12+s -d18+s",        // фейк + каскад дизордера
        // --- дешёвые одиночные (для «лёгких» сетей) ---
        "-d1+s",
        "-s1+s -d3+s",
        "-s2",
        "-f1+s -t8",
        // --- UDP/QUIC-десинк для видео (ролик YouTube по QUIC) ---
        "-a1 -t8",
        "-a2 -t5",
        // --- комбо: TCP-каскад для сайта + UDP-десинк для ролика одновременно ---
        "-s1+s -d5+s -d11+s -d17+s -a1 -t8",
    )

    /**
     * Глубокий перебор: каскады разной ПЛОТНОСТИ (step) и ГЛУБИНЫ (max),
     * свипы TTL для fake, семейство tlsrec и UDP/QUIC. Всё позиционно-независимо.
     */
    fun deep(): List<String> {
        val out = LinkedHashSet<String>()

        // (0) ПРИОРИТЕТ: комбо TCP+UDP. Единственный класс, проходящий и страницу
        //     (TCP/TLS), и видео (QUIC/UDP). Ставим первыми, чтобы рабочий вариант
        //     нашёлся и авто-применился в начале перебора — можно сразу остановить.
        out.addAll(combos())

        // (1) Дизордер-каскады: плотность 2/3/4, доходят до 12/18/24 — любое
        //     блок-слово в этом диапазоне гарантированно разрывается.
        for (step in listOf(2, 3, 4)) for (max in listOf(12, 18, 24))
            out.add(cascade("d", 1, step, max))

        // (2) Сплит-каскады (мягкий вариант).
        for (step in listOf(2, 3, 4)) for (max in listOf(12, 18, 24))
            out.add(cascade("s", 1, step, max))

        // (3) Граница SNI (сплит 1) + дизордер-каскад — устойчиво и на Linux, и на Win.
        for (max in listOf(12, 18, 24))
            out.add("-s1+s " + (3..max step 4).joinToString(" ") { "-d$it+s" })

        // (4) tlsrec: одиночный, свип позиции и — главное — КАСКАДЫ записей
        //     (самый пробивной класс против SNI-блэкхола). Разная плотность/глубина.
        out.add("-r1+s"); out.add("-r2+s"); out.add("-r4+s"); out.add("-r-1")
        for (step in listOf(3, 4, 5)) for (max in listOf(13, 18, 24))
            out.add(cascade("r", 1, step, max))
        // auto-gated tlsrec-каскады (безопаснее для не-заблокированных сайтов).
        out.add("-A torst -T 3 -r1+s -r5+s -r9+s -r13+s")
        out.add("-A torst -T 3 -r1+s -r6+s -r11+s -r16+s -r21+s")
        // tlsrec + каскад сплитов/дизордера (комбо, вдруг чистый tlsrec не добьёт).
        out.add("-r1+s -s4+s -s10+s -s16+s")
        out.add("-r1+s -d4+s -d10+s -d16+s")

        // (5) fake со свипом TTL (дистанция до DPI неизвестна) + каскадные комбо.
        for (t in 1..8) out.add("-f1+s -t$t")
        for (t in listOf(2, 3, 4, 5)) out.add("-d1+s -f-1 -t$t")     // disorder+fake, README-Linux-стиль
        for (t in listOf(2, 3, 4)) out.add("-f1+s -t$t -d6+s -d12+s -d18+s")

        // (6) UDP/QUIC-десинк для видео.
        for (c in listOf(1, 2, 3)) for (t in listOf(2, 4, 6, 8)) out.add("-a$c -t$t")

        // (7) TCP-каскад + UDP одновременно (сайт по TCP, ролик по QUIC).
        out.add("-s1+s -d5+s -d11+s -d17+s -a1 -t8")
        out.add("-d1+s -d7+s -d13+s -d19+s -a2 -t5")

        out.addAll(curated())
        return out.toList()
    }
}
